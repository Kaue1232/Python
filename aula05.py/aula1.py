# Manipulando Texto

#Fatiamento de string

texto = 'Python é incrível' #16

print(texto[0:7]) #Irá imprimir somente python, com 6 caracteres +1
print(texto[9]) #Imprimir somente o caractere 9
print(texto[0:16:2]) #Vai imprimir todos os caracteres de 0 a 16 pulando de 2 em 2. Ex: Kauê Kelvyn ----- 'Ku evn'
print(texto[:7]) #Imprimir todos os caracteres de 0 até 6


